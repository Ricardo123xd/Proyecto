     /**
     * Este metodo permite limpiar las cajas de texto y ubicar el focus en la caja de texto del nombre
     */
    private void limpiarFormulario(){
        //asignmaos el string -1 a la caja de texto de idCancion
        txtIdCancion.setText("-1");
        
        //limpiamos las otras cajas de texto
        txtNombre.setText("");
        txtFecha.setText("");
        txtDuracion.setText("");
        
        
        //ubicamos el focus en la caja de texto del nombre
        txtNombre.requestFocusInWindow();
    }//fin del metodo limpiarFormulario
    
    /**
     * valida los datos de entrada del formulario
     * @return true si todos los datos son validados correctamente, false en caso contrario
     */
    private boolean validar(){
        boolean validacion = false;
        
        idCancion = Integer.parseInt(txtIdCancion.getText());
        nombre = txtNombre.getText().trim();
        fecha = txtFecha.getText().trim();
        duracion = Integer.parseInt(txtDuracion.getText());
        
        if(nombre.equals("")){
            JOptionPane.showMessageDialog(null, "Especifica el nombre de la cancion");
            txtNombre.requestFocusInWindow();
            return validacion;
        }
        if(Integer.toString(duracion).equals("")){
            JOptionPane.showMessageDialog(null, "Especifica la duracion de la cancion (segundos)");
            txtDuracion.requestFocusInWindow();
            return validacion;
        }
        if(fecha.equals("")){
            JOptionPane.showMessageDialog(null, "Especifica la fecha de la cancion (dd/mm/aa)");
            txtFecha.requestFocusInWindow();
            return validacion;
        }
        return true;
    }//fin del metodo validar

    /**
     * Imprime un mensaje de error personalizado para aquellos errores que son
     * producidos por el acceso a la base de datos
     * @param ex objeto de tipo DAOException
     */
    private void imprimirMensajeDeErrorDAO(DAOException ex){
        //si getMessage existe obtenemos su valor
        String mensajeError;
        
        try{
            mensajeError = ex.getCause().getMessage();
        }catch(NullPointerException error){
            mensajeError = "";
        }
        
        JOptionPane.showMessageDialog(null, ex.getMessage() + "\n"+ "Mensaje: "+ex.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
    }//fin del metodo imprimirMensajeDeErrorDAO
    
    
