    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //llamamos al metodo validar para ver si procede guardar los datos
        if (validar()) {
            //si idCancion = -1 entonces insertamos el registro
            if (idCancion == -1) {
                //llamamos al constructor para crear un objeto de tipo Canciones
                Canciones miCancion = new Canciones(nombre, fecha, duracion);
                try {
                    manager.getCancionDAO().insertar(miCancion);
                    txtIdCancion.setText(Integer.toString(miCancion.getDuracion()));
                    JOptionPane.showMessageDialog(null, "Los datos han sido guardados");
                } catch (DAOException ex) {
                    imprimirMensajeDeErrorDAO(ex);
                }
            } else {//si es diferente de -1 quiere decir que se esta realizando una modificacion

                //llamamos al constructor para crear un objeto de tipo Canciones
                Canciones miCancion = new Canciones(idCancion, nombre, fecha, duracion);
                try {
                    manager.getCancionDAO().modificar(miCancion);
                    JOptionPane.showMessageDialog(null, "Los cambios han sido guardados");
                } catch (DAOException ex) {
                    imprimirMensajeDeErrorDAO(ex);
                }
            }//fin del else
        }//fin del if validar
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //solo se puede eliminar si la cancion es diferente de -1
        if (!txtIdCancion.getText().equals("-1"));
        {
            int idCancion = Integer.parseInt(txtIdCancion.getText());

            int respuesta = JOptionPane.showConfirmDialog(null, "Deseas eliminar la cancion con id = "
                    + idCancion + "?", "Confirmar", 0);

            if (respuesta == 0) {
                try {
                    //eliminamos la cancion
                    manager.getCancionDAO().eliminar(idCancion);
                    //si no, ocurre una excepci√≥n
                    JOptionPane.showMessageDialog(null, "La cancion ah sido eliminada");
                } catch (DAOException ex) {
                    imprimirMensajeDeErrorDAO(ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Busca una cancion para poder eliminarla");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try{
            //obtenemos el idAutor a buscar
            int idABuscar = (Integer) Integer.parseInt(txtBuscarPorId.getText());
            
            //obtenemos los datos de la cancion y lo asignamos al objeto miCancion
            Canciones miCancion = manager.getCancionDAO().obtener(idABuscar);
            
            //mostramos los datos en a caja de texto
            txtIdCancion.setText(Integer.toString(miCancion.getIdCancion()));
            txtNombre.setText(miCancion.getNombre());
            txtFecha.setText(miCancion.getFechaCreacion());
            txtDuracion.setText(Integer.toString(miCancion.getDuracion()));
            
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Especifica un numero entero", "Error",
                    JOptionPane.ERROR_MESSAGE);
            txtBuscarPorId.requestFocusInWindow();
            txtBuscarPorId.selectAll();
        } catch (DAOException ex) {
            imprimirMensajeDeErrorDAO(ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

